create table if not exists ПРЕПОДАВАТЕЛЬ(
    ЛичныйНомер text not null prof_pk primary key,
    Фамилия text not null,
    Должность text,
    Кафедра text,
    Специальность text,
    ТелефонДомашний int
);
create table if not exists ПРЕДМЕТ(
    КодовыйНомерПредмета text not null subk_pk primary key,
    НазваниеПремета text not null,
    КоличествоЧасов int,
    Специальность text,
    Семестр int
);
create table if not exists СТУДЕНЧЕСКАЯ_ГРУППА(
    КодовыйНомерГруппы text not null  group_pk primary key,
    НазваниеГруппы text not null,
    КоличествоЧеловек int,
    Специальность text,
    ФамилияСтаросты text
);
create table if not exists ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ(
    КодовыйНомерГруппы text not null tech_pk primary key,
    КодовыйНомерПредмета text not null subj_pk primary key,
    ЛичныйНомер text not null prof_pk primary key,
    НомерАудитории int
);

