    where ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ.НомерАудитории = 210;
select ПРЕДМЕТ.НазваниеПремета, СТУДЕНЧЕСКАЯ_ГРУППА.НазваниеГруппы from ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ
    inner join ПРЕДМЕТ on ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ.КодовыйНомерПредмета = ПРЕДМЕТ.КодовыйНомерПредмета
    inner join СТУДЕНЧЕСКАЯ_ГРУППА on ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ.КодовыйНомерГруппы = СТУДЕНЧЕСКАЯ_ГРУППА.КодовыйНомерГруппы
    where НомерАудитории > 100 and НомерАудитории < 200;

