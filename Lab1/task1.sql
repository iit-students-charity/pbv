select * from СТУДЕНЧЕСКАЯ_ГРУППА
    where Специальность = 'ЭВМ';
select ЛичныйНомер, НомерАудитории from ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ
    where КодовыйНомерПредмета = '18П';
select ПРЕДМЕТ.КодовыйНомерПредмета, ПРЕДМЕТ.НазваниеПремета from ПРЕДМЕТ
    inner join ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ on ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ.КодовыйНомерПредмета = ПРЕДМЕТ.КодовыйНомерПредмета
    inner join ПРЕПОДАВАТЕЛЬ on ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ.ЛичныйНомер = ПРЕПОДАВАТЕЛЬ.ЛичныйНомер
    where ПРЕПОДАВАТЕЛЬ.Фамилия = 'Костин';
select ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ.КодовыйНомерГруппы from ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ
    inner join ПРЕПОДАВАТЕЛЬ on ПРЕПОДАВАТЕЛЬ.ЛичныйНомер = ПРЕПОДАВАТЕЛЬ_ПРЕПОДАЁТ_ПРЕДМЕТЫ_В_ГРУППАХ.ЛичныйНомер
    where ПРЕПОДАВАТЕЛЬ.Фамилия = 'Фролов';
select * from ПРЕДМЕТ 
    where Специальность like '%АСОИ%';
select ПРЕПОДАВАТЕЛЬ.Фамилия from ПРЕПОДАВАТЕЛЬ
